/**
 * op_add - performs addition of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: sum of @a and @b
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - performs subtraction of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: difference between @a and @b
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - performs multiplication of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: product of @a and @b
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - performs division of two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: quotient of @a divided by @b
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - performs modulo operation on two integers
 * @a: first integer
 * @b: second integer
 *
 * Return: remainder of @a divided by @b
 */

int op_mod(int a, int b)
{
	return (a % b);
}
